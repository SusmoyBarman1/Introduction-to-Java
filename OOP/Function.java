public class Function
{
    int square(int a)
    {
        return a*a;
    }
    int multiplication(int a,int b)
    {
        return a*b;
    }
    int multiplication(int a,int b,int c)
    {
        return a*b*c;
    }
    double multiplication(double a,double b)
    {
        return a*b;
    }
}